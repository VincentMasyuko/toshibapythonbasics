# loops used in dsa

cars = ["nissan", "toyota", "mercedes", 'Vw']


for i in cars:
    print(i)
    if i == "toyota":
        break
for x in range(2, 6):
    print(x)

color = ["red", "big", "tasty"]
fruits = ["apples", "oranges", "cherry"]
# nested loop
for rangi in color:
    for matunda in fruits:
        print(rangi, matunda)
