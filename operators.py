# arithmetic operator

x = 7
y = 3
print(x + y)  # add
print(x - y)  # sub
print(x * y)  # multiply
print(x / y)  # divide
print(x % y)  # remeinder

# comparisos operator

a = 12
b = 34

print(f"{a} is equal to {b} {a == b}")
print(f"{a} is not equal to {b} {a != b}")
print(f"{a} is greater than {b} {a > b}")
print(f"{a} is greater than or equals to {b} {a >= b}")
print(f"{a} is less than {b} {a < b} ")
print(f"{a} is less than or equal to {b} {a < b} ")

